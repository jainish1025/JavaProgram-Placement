package com.jainish;
// Logical Operator
public class LogicalOperater {
    public static void main(String[] args) {
        int a = 10;
        int b = 20;
        int c = 10;
        System.out.println("a == b && a == c : " + (a == b && a == c));
        System.out.println("a == b || a == c : " + (a == b || a == c));
        System.out.println("!(a == b) : " + !(a == b));


    }
}
