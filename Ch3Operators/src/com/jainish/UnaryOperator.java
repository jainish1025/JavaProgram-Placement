package com.jainish;
  //Unary Operator
public class UnaryOperator {
    public static void main(String[] args) {
        //Increment Operator
        int a = 5;
        System.out.println("Post increment  a++ : " + (a++)); // used first then increment
        System.out.println("Pre  Increment  ++a : " + (++a)); // increment first then used

        //Decrement Operator
        int b = 10;
        System.out.println("Post Decrement a-- : " + (b--)); // used first then decrement
        System.out.println("Pre  Decrement --a : " + (--b)); // decrement first then used



    }
}
